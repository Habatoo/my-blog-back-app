package io.github.habatoo.dto.request;

/**
 * DTO для запроса создания комментария.
 * <p>
 * Содержит данные для создания/редактирования комментария к посту.
 * Используется как входящие данные для API endpoints создания/редактирования комментариев.
 * </p>
 *
 * @param id     идентификатор комментария (обязательное поле, не может быть null или пустым для существующего)
 * @param text   текст комментария (обязательное поле, не может быть null или пустым)
 * @param postId идентификатор поста, к которому добавляется комментарий (обязательное поле, не может быть null)
 */
public record CommentRequest(
        Long id,
        String text,
        Long postId
) {
}
